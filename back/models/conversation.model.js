import mongoose from "mongoose";

const conversaionSchema = new mongoose.Schema(
  {
    currentconvo: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
      },
    ],
    messages: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "Message",
        default: [],
      },
    ],
  },
  { timestamps: true }
);

const Conversation = mongoose.model("Conversation", conversaionSchema);

export default Conversation;
